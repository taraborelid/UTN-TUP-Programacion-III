package enumeraciones;


/*
 Los enums son un tipo especial de clase que permite definir
 un conjunto fijo de constantes nombradas, lo que mejora la
 legibilidad y seguridad del código.
Los enums son útiles cuando necesitas representar un grupo de valores constantes (como días, meses, estados, etc.).
Cada constante (LUNES, MARTES, etc.) es una instancia del enum Dias y se comporta como un objeto (
aunque no se vea aquí, pueden tener atributos y métodos).
 */


public enum Dias {


    LUNES,
    MARTES,
    MIERCOLES,
    JUEVES,
    VIERNES,
    SABADO,
    DOMINGO

}
